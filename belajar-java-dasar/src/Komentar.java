public class Komentar {
    public static void main(String[] args) {

        System.out.println(sum(10, 10));

    }
    /*
        Ini untuk Komentar Multi Line dalam Java
     */
    // ini untuk komemtar Singel Line dalam Java

    //dibawah ini adalah, komentar yang biasa digunakan untuk membuat dokumentasi
    /**
     * Method untuk Menjumlahkan value1 dan value2
     * @param value1 angka pertama
     * @param value2 angka kedua
     * @return hasil penjumlahan value1 dan value2
     */
    static int sum(int value1, int value2){
        //Jumlahkan Nilai antara value1 + value2
        return value1 + value2;
    }
}
